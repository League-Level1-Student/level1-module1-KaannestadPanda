
public class Taco {

	private String meat;
	private String sauce;
	public static void main(String[] args) {
		Taco name=new Taco();
		name.setMeat("h");
		name.setSauce("j");
		
		
	}
	
	String getMeat() {
		return meat;
	}
	
	String getSauce() {
		return sauce;
	}
	
	void setMeat(String mat) {
		meat.equals(mat);
	}
	
	void setSauce(String sauc) {
		sauce.equals(sauc);
	}
	
}
